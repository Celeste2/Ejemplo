    private void llenartabla(ArrayList<String> pdatos){
        logger.info("llenar tabla");
        String[] columnas = new String[21];
        columnas[0]="Activo";
        for (int x = 1; x <=20; x++){
            String incremento;
            incremento = Integer.toString(x);
            columnas[x]="A単o ".concat(incremento);
        }
        jTable1.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
        DefaultTableModel tableModel = new DefaultTableModel(columnas, 0);
        logger.info("viendo el tama単o del arreglo");
        logger.info(Integer.toString(pdatos.size()-1));
         int ano=0;
         Object[] data = new Object[21];
         data[0]=jNombreActivo.getText();
        for (String i: pdatos) {
            
            ano++;
            
             
            data[ano]=i;
         
         
         
          
        
        
        }
         tableModel.addRow(data);
        this.jTable1.setModel(tableModel);
        logger.info("Antes de repaint");
        this.jTable1.repaint();
         tableModel.fireTableDataChanged();
        logger.info("Despues del repaint");
    }   
    
