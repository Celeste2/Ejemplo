    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
         double depreciacion;
                int valoractivo=0;
                int valorrescate=0;
                int vidautil=0;
                
        
        String msj="";
        //primero validamos
        if (jValorActivo.getText().equals(""))
        {
            msj="Debe digitar el valor del activo";
        }
        else if (jValorResidual.getText()=="") 
        {
            msj="Debe digitar el valor residual del activo";
        }
        else if (jAnosVida.getText()=="") {
            msj="Debe digitar los a単os de vida";
        }
        else 
        {
            if (!msj.equals("")) {
                JOptionPane.showMessageDialog(this, msj);
            }
            else 
            {
             vidautil=Integer.parseInt(jAnosVida.getText());
             valoractivo=Integer.parseInt(jValorActivo.getText());
             valorrescate=Integer.parseInt(jValorResidual.getText());
             depreciacion=this.depreciacion(valoractivo, valorrescate, vidautil);
             ArrayList<String> datos= new ArrayList();
             
             logger.info("Viendo los datos calculados");
             logger.info(Double.toString(depreciacion));
             for (int x=0;x < vidautil; x++)
             {
                 datos.add(Double.toString(depreciacion));
                  
             }
             logger.info(datos.toString());
             llenartabla(datos);
            }
            
            
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

